package com.github.julyss2019.mcsp.julyguild.placeholder;

public class Placeholder {
    private String key;
    private String value;

    Placeholder(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
}
